def make_hand(spades='', hearts='', diamonds='', clubs=''):
    hand = []
    for suit, cards in [('вҷ ',spades),('вҷҘ',hearts),('вҷҰ',diamonds),('вҷЈ',clubs)]:
        for r in cards:
            hand.append({'s':suit,'r':r})
    return hand

