import sys, io
sys.stdout = io.TextIOWrapper(sys.stdout.buffer, encoding='utf-8')
sys.path.insert(0, 'D:/bridge-teacher')
from bidding_engine import compute_auction
from bridge_teacher import cfg

cfg['majorLen']=5; cfg['ntMin']=15; cfg['ntMax']=17; cfg['minorLen']=3

errors = []

def make_hand(spades='', hearts='', diamonds='', clubs=''):
    hand = []
    for suit, cards in [('♠',spades),('♥',hearts),('♦',diamonds),('♣',clubs)]:
        for r in cards: hand.append({'s':suit,'r':r})
    return hand

EW_E = make_hand('T97','976','T987','876')
EW_W = make_hand('862','543','654','T932')

def run(name, hands, dealer, expected_ns):
    c = dict(cfg)
    auction, players = compute_auction(hands, dealer, c)
    ns_bids = [auction[i] for i in range(len(auction)) if players[i] in ('N','S')]
    if ns_bids == expected_ns:
        print(f'[OK] {name}')
    else:
        print(f'[FAIL] {name}')
        print(f'  צפוי:   {expected_ns}')
        print(f'  קיבלנו: {ns_bids}')
        errors.append(name)

# ════════════════════════════════════════════════════════════
# 1♥ — תמיכה ישירה
# ════════════════════════════════════════════════════════════

# N=13 (5♥), S=7 (3♥) → 1♥-2♥-פס
# N: ♠K32 ♥AKJ54 ♦K32 ♣32 = K+A+K+J+K = 3+4+3+1+3 = 14 נק'
# S: ♠Q54 ♥Q76 ♦J54 ♣J876 = Q+Q+J+J = 2+2+1+1 = 6 נק', 3♥
run('1♥ → 2♥ → פס (N=14, S=6)',
    {'N': make_hand('K32','AKJ54','K32','32'),
     'S': make_hand('Q54','Q76','J54','J876'),
     'E': EW_E, 'W': EW_W}, 'N',
    ['1♥','2♥','פס'])

# N=16 (5♥, לא מאוזן), S=6 (3♥) → 1♥-2♥-3♥-פס
# N: ♠A2 ♥AKJ54 ♦Q432 ♣Q2 = A+A+K+J+Q+Q = 4+4+3+1+2+2 = 16 נק', 5422 (לא מאוזן → לא 1NT)
# S: ♠J54 ♥Q76 ♦J876 ♣Q54 = J+Q+J+Q = 1+2+1+2 = 6 נק', 3♥
run('1♥ → 2♥ → 3♥ → פס (N=16, S=6)',
    {'N': make_hand('A2','AKJ54','Q432','Q2'),
     'S': make_hand('J54','Q76','J876','Q54'),
     'E': EW_E, 'W': EW_W}, 'N',
    ['1♥','2♥','3♥','פס'])

# N=16 (5♥, לא מאוזן), S=9 (3♥) → 1♥-2♥-3♥-4♥
# S: ♠J54 ♥Q76 ♦KJ6 ♣Q654 = J+Q+K+J+Q = 1+2+3+1+2 = 9 נק'
run('1♥ → 2♥ → 3♥ → 4♥ (N=16, S=9)',
    {'N': make_hand('A2','AKJ54','Q432','Q2'),
     'S': make_hand('J54','Q76','KJ6','Q654'),
     'E': EW_E, 'W': EW_W}, 'N',
    ['1♥','2♥','3♥','4♥','פס'])

# N=14, S=10 (3♥) → 1♥-3♥-4♥-פס
# N: ♠K32 ♥AKJ54 ♦K32 ♣32 = K+A+K+J+K = 3+4+3+1+3 = 14 נק'
# S: ♠A54 ♥Q76 ♦Q54 ♣J876 = A+Q+Q+J = 4+2+2+1 = 9 נק'... צריך 10
# S: ♠A54 ♥Q76 ♦Q54 ♣KJ76 = A+Q+Q+K+J = 4+2+2+3+1 = 12 נק', 3♥
run('1♥ → 3♥ → 4♥ (N=14, S=12)',
    {'N': make_hand('K32','AKJ54','K32','32'),
     'S': make_hand('A54','Q76','Q54','KJ76'),
     'E': EW_E, 'W': EW_W}, 'N',
    ['1♥','3♥','4♥','פס'])

# N=14 (5♥), S=8 (3♥) → 1♥-2♥-פס (S בטווח 6-9 → מכריז 2♥)
# S: ♠Q54 ♥K76 ♦Q54 ♣J876 = Q+K+Q+J = 2+3+2+1 = 8 נק', 3♥ → 2♥
run('1♥ → 2♥ → פס (N=14, S=8)',
    {'N': make_hand('K32','AKJ54','K32','32'),
     'S': make_hand('Q54','K76','Q54','J876'),
     'E': EW_E, 'W': EW_W}, 'N',
    ['1♥','2♥','פס'])

# ════════════════════════════════════════════════════════════
