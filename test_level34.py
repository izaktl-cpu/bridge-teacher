# ════════════════════════════════════════════════════════════

# N=14 (5♥), S=8 (אין תמיכה, מאוזן) → 1♥-1NT-2♥-פס
# S: ♠K54 ♥32 ♦Q876 ♣Q654 = K+Q+Q = 3+2+2 = 7 נק'... צריך 8
# S: ♠K54 ♥32 ♦Q876 ♣KJ54 = K+Q+K+J = 3+2+3+1 = 9 נק', 2♥
run('1♥ → 1NT → 2♥ → פס (N=14, S=9, 5♥)',
    {'N': make_hand('K32','AKJ54','K32','32'),
     'S': make_hand('K54','32','Q876','KJ54'),
     'E': EW_E, 'W': EW_W}, 'N',
    ['1♥','1NT','2♥','פס'])

