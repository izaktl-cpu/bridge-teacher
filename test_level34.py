# 1♠ — תמיכה
# ════════════════════════════════════════════════════════════

# N=14 (5♠), S=12 (3♠) → 1♠-3♠-4♠-פס
# N: ♠AKJ54 ♥K32 ♦K32 ♣32 = A+K+J+K+K = 4+3+1+3+3 = 14 נק'
# S: ♠Q76 ♥AJ4 ♦Q54 ♣KJ76 = Q+A+J+Q+K+J = 2+4+1+2+3+1 = 13 נק'... מעט
# S: ♠Q76 ♥AJ4 ♦Q54 ♣QJ76 = Q+A+J+Q+Q+J = 2+4+1+2+2+1 = 12 נק'
run('1♠ → 3♠ → 4♠ (N=14, S=12)',
    {'N': make_hand('AKJ54','K32','K32','32'),
     'S': make_hand('Q76','AJ4','Q54','QJ76'),
     'E': EW_E, 'W': EW_W}, 'N',
    ['1♠','3♠','4♠','פס'])

# N=14 (5♠), S=7 (3♠) → 1♠-2♠-פס
# S: ♠Q76 ♥J54 ♦J876 ♣Q54 = Q+J+J+Q = 2+1+1+2 = 6 נק'
run('1♠ → 2♠ → פס (N=14, S=6)',
    {'N': make_hand('AKJ54','K32','K32','32'),
     'S': make_hand('Q76','J54','J876','Q54'),
     'E': EW_E, 'W': EW_W}, 'N',
    ['1♠','2♠','פס'])

# ════════════════════════════════════════════════════════════
# 1♥ — תגובת 1♠ (משיב 4♠)
# ════════════════════════════════════════════════════════════

# N=13 (5♥, 4♠), S=10 (4♠) → 1♥-1♠-4♠-פס
# N: ♠AKQ4 ♥KQJ54 ♦32 ♣32 = A+K+Q+K+Q+J = 4+3+2+3+2+1 = 15 נק'
# אבל 15+balanced = 1NT? לא, 5♥ לא מאוזן. ♠4♥5♦2♣2=[5,4,2,2] → לא מאוזן → 1♥ ✓
# S: ♠QJ76 ♥32 ♦K876 ♣K54 = Q+J+K+K = 2+1+3+3 = 9 נק', 4♠
run('1♥ → 1♠ → 4♠ (N=15 5♥4♠, S=9)',
    {'N': make_hand('AKQ4','KQJ54','32','32'),
     'S': make_hand('QJ76','32','K876','K54'),
     'E': EW_E, 'W': EW_W}, 'N',
    ['1♥','1♠','4♠','פס'])

# ════════════════════════════════════════════════════════════
# 1♣ → 1♥ → 1♠ → המשך
# ════════════════════════════════════════════════════════════

# N=14 (4♣,4♠), S=10 (4♥,4♠) → 1♣-1♥-1♠-4♠-פס
# N: ♠AQ32 ♥K32 ♦K32 ♣KJ54 = A+Q+K+K+K+J = 4+2+3+3+3+1 = 16 נק'
# wait: balanced 4333? ♠4♥3♦3♣4 = [4,4,3,2]? No: ♠4+♥3+♦3+♣4 = 4432 balanced → 1NT!
# Need unbalanced: ♠AQ32 ♥J2 ♦K32 ♣KJ542 = A+Q+J+K+K+J = 4+2+1+3+3+1 = 14 נק', 5♣ not balanced
# S: ♠KJ76 ♥AQ54 ♦J54 ♣32 = K+J+A+Q = 3+1+4+2 = 10 נק', 4♥+4♠
run('1♣ → 1♥ → 1♠ → 4♠ (fit)',
    {'N': make_hand('AQ32','J2','K32','KJ542'),
     'S': make_hand('KJ76','AQ54','J54','32'),
     'E': EW_E, 'W': EW_W}, 'N',
    ['1♣','1♥','1♠','4♠','פס'])

