cfg['majorLen']=5; cfg['ntMin']=15; cfg['ntMax']=17; cfg['minorLen']=3

SUITS     = ['♠','♥','♦','♣']
ALL_RANKS = ['A','K','Q','J','T','9','8','7','6','5','4','3','2']
RANK_HCP  = {'A':4,'K':3,'Q':2,'J':1}

passed = 0; total = 0; errors = []
_used = set()

def reset():
    global _used; _used = set()

def hand(dist, hons=None):
    """בנה יד: dist=אורכי סדרות, hons=כרטיסי כבוד. קטנים ממולאים מהנמוך."""
    global _used
    if hons is None: hons = {}
    result = []
    for suit in SUITS:
        n = dist.get(suit, 0)
        hr = list(hons.get(suit, ''))
        for r in hr:
            key = (suit, r)
            assert key not in _used, f"קונפליקט: {suit}{r}"
            _used.add(key); result.append({'s': suit, 'r': r})
        needed = n - len(hr)
        for r in reversed(ALL_RANKS):
            if needed <= 0: break
            key = (suit, r)
            if key not in _used and r not in hr:
                _used.add(key); result.append({'s': suit, 'r': r}); needed -= 1
        assert needed == 0, f"אין קלפים ל-{suit}"
    assert len(result) == 13
    return result

def deal(n, s):
    """EW מקבלים קלפים שנותרו; E ממוין לפי HCP עולה (שקט)."""
    used = {(c['s'], c['r']) for c in n + s}
    remaining = sorted(
        [{'s': su, 'r': ra} for su in SUITS for ra in ALL_RANKS if (su, ra) not in used],
        key=lambda c: RANK_HCP.get(c['r'], 0)
    )
    return {'N': n, 'E': remaining[:13], 'S': s, 'W': remaining[13:]}

def ns_bids(auction, players):
    return [auction[i] for i, p in enumerate(players) if p in ('N','S')]

def fmt(h):
    return ' '.join(
        f"{s}{''.join(sorted([c['r'] for c in h if c['s']==s], key=lambda r: ALL_RANKS.index(r))) or '—'}"
        for s in SUITS)

def chk(title, d, expected, dealer='N'):
    global passed, total
    total += 1
    auction, players = compute_auction(d, dealer, dict(cfg))
    actual = ns_bids(auction, players)
    k = len(expected)
    ok = len(actual) >= k and actual[:k] == list(expected)
    if ok:
        passed += 1
        print(f'  ✓ {title}')
    else:
        got = ' → '.join(actual[:k+1]) if actual else '?'
        exp = ' → '.join(expected)
        errors.append(f'{title}: {got} במקום {exp}')
        print(f'  ✗ {title}: {got} במקום {exp}')
        print(f'    N({hcp(d["N"])}): {fmt(d["N"])}')
        print(f'    S({hcp(d["S"])}): {fmt(d["S"])}')

# ── מאקרו: בנה deal ישיר ─────────────────────────────────────────────────────
def mk(nd, nh, sd, sh):
    reset()
    n = hand(nd, nh)
    s = hand(sd, sh)
    return deal(n, s)


